    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        try{
            s = conntodb.createStatement();
            int memberID = Integer.parseInt(ID.getText());
            
            String SQL = "SELECT * FROM members WHERE ID = '"+memberID+"'";
            rSet = s.executeQuery(SQL);
            
            //if is available
            if(rSet.next()){
                mName.setText(rSet.getString("userName"));
                mPassword.setText(rSet.getString("userPassword"));
                mEmail.setText(rSet.getString("userEmail"));
            }else{
                JOptionPane.showMessageDialog(null, "Member not found!");
            }
            
        }catch(SQLException | NumberFormatException e){
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_searchButtonActionPerformed

