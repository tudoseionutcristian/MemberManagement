    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
       try{
            s = conntodb.createStatement();
            
            int memberID = Integer.parseInt(ID.getText());
            String memberEmail = mEmail.getText();
            String memberPassword = mPassword.getText();
            String memberName = mName.getText();

        
            String SQL = "UPDATE members SET userEmail = '"+memberEmail+"', userPassword ='"+memberPassword+"', userName = '"+memberName+"' "
                    + "WHERE ID ='"+memberID+"'";
            s.executeUpdate(SQL);
            JOptionPane.showMessageDialog(null, "Data updated.");
        }catch(SQLException | HeadlessException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_saveButtonActionPerformed

