    private void addMemberButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addMemberButtonActionPerformed
        // TODO add your handling code here:
        try{
            s = conntodb.createStatement();
            String memberEmail = mEmail.getText();
            String memberPassword = mPassword.getText();
            String memberName = mName.getText();
            
            String SQL = "INSERT INTO members(userEmail, userPassword, userName) "
                    + "VALUES('"+memberEmail+"', '"+memberPassword+"', '"+memberName+"')";
            s.executeUpdate(SQL);
            JOptionPane.showMessageDialog(null, "New member added");
        }catch(SQLException | HeadlessException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_addMemberButtonActionPerformed

